//! Testeo con Jest
// un test es una pequeña función que ejecuta nuestro código y comprar el resultado con lo que esperamos

//! JEST
// framework de testeo ---> Otras:

    // Mocha - muy usado en Node.js
    // Chai - se usa con Mocha
    // Vitest - similiar a Jest pero para proyectos Vite
    // AVA - ejecuta testeo en paralelo
    // Cypress - especializado en frontend

//! Tipos de tests

//* Unitario
// Comprueba una función o un módulo específico
// EJ: sumar()

//* Integración
// Comprueba cómo interactúan los módulos
//EJ: sumar() + restar() juntos

//* E2E (End-to-End)
// Prueba desde el punto de vista del usuario
// EJ: Prueba el flujo completo de login, logout, registro, ...